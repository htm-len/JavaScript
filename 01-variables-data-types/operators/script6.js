// 1. Arthmetic operators

let x; 

x = 10 + 10; // 20 (Addition operator)
x = 10 - 5; // 5  (Subtraction operator)
x = 10 * 5; // 50 (Multiplication operator)
x = 10 / 5; // 2 (Division operator)
x = 10 % 3; // 1 (Modulus operator) - remainder of division

// Concatenation operator
x = 'Hello' + ' ' + 'World'; // Hello World

// exponential operator
x = 2 ** 3; // 8

// increment operator
x = 10;
x++; // 11

//decrement operator
x--; // 10

// 2. Assignment operators
x = 10; // 10 // assignment operator
x += 5; // 15 // addition assignment operator
x -= 5; // 10 // subtraction assignment operator
x *= 5; // 50 // multiplication assignment operator
x /= 5; // 10 // division assignment operator
x %= 3; // 1 // modulus assignment operator
x **= 3; // 1000 // exponential assignment operator

// 3. Comparison operators
x = 2 == '2'; // true // equal to operator

x = 2 === '2'; // true // strict equal to operator

x = 2 != '2'; // false // not equal to operator

x = 2 !== '2'; // false // strict not equal to operator

x = 2 > 1; // true // greater than operator

x = 2 < 1; // false // less than operator

x = 2 >= 2; // true // greater than or equal to operator

x = 2 <= 2; // true // less than or equal to operator

console.log(x); // undefined

